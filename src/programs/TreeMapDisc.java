package programs;

import java.util.TreeMap;

public class TreeMapDisc {
	
	public static void main(String[] args) {
		
		
		TreeMap<Integer, Character> tm = new TreeMap<Integer, Character>();
		
		tm.put(10, 'c');
		tm.put(8, 't');
		tm.put(2, '%');
		tm.put(78, '1');
		tm.put(98, '$');
		
		
		System.out.println(tm);
		
		
		
	}

}

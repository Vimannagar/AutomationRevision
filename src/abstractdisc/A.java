package abstractdisc;

public abstract class A {
	
	public A()
	{
		System.out.println("Constructor of abstract class A");
	}
	
	

	
	public void m1() {
		System.out.println("m1 method from A class");
	}
	
	public void m2() {
		System.out.println("m2 method from A class");
	}
	
	
	
	
	public abstract void m3();
	
	

}
